// keyword var : Redeclare, Reassign, 

var nama = "123"; // deklarasi variable : tahap pertama ketika membuat / mendefinisikan variable.
var nama = "Edo";

nama = "Zidane"; //Reassign
console.log(nama); // console.log() --> nilai yang ingin dilihat.


//keyword let : Non redeclare, reassign

let usia = 30;
console.log(usia); //let usia = 25; tidak bisa memiliki nama var sama
usia = 25
console.log(usia);

let job;
console.log(job);

